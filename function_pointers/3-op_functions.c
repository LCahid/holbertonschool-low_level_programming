#include <stdlib.h>
#include <stdio.h>
/**
  * op_add - init
  * @a: a
  * @b: b
  * Return: return
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - sub
  * @a: a
  * @b: b
  * Return: return
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - sub
  * @a: a
  * @b: b
  * Return: return
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - sub
  * @a: a
  * @b: b
  * Return: return
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - sub
  * @a: a
  * @b: b
  * Return: return
  */
int op_mod(int a, int b)
{
	return (a %  b);
}
